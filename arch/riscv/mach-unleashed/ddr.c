static inline void board_ddr_boot_delay(void)
{
	int i;

	/* These take like 16 cycles to actually propogate. We can't go
	 * sending stuff before they come out of reset. So wait.
	 * TODO:
	 *  Add a register to read the current reset states, or DDR Control
	 *  device?
	 */
	for (i = 0; i < 256; i++)
		nop();
}

void board_ddr_init(void)
{
	const uint64_t ddr_size = DDR_SIZE;
	const uint64_t ddr_end = DDR_BASE + ddr_size;

	clk_enable(ddrctrlclk);

	/* Release DDR reset */
	ddrc_reset();
	wmb(); /* HACK to get the '1 full controller clock cycle */
	ddrc_axi_reset();
	ddrc_ahb_reset();
	ddr_phy_reset();
	wmb(); /* HACK to get the '1 full controller clock cycle */

	board_ddr_boot_delay();

	ux00ddr_writeregmap(DDRC_BASE, ddr_ctl_settings, ddr_phy_settings);
	ux00ddr_disableaxireadinterleave(DDRC_BASE);
	ux00ddr_disableoptimalrmodw(DDRC_BASE);
	ux00ddr_enablewriteleveling(DDRC_BASE);
	ux00ddr_enablereadleveling(DDRC_BASE);
	ux00ddr_enablereadlevelinggate(DDRC_BASE);
	if (ux00ddr_getdramclass(DDRC_BASE) == DRAM_CLASS_DDR4)
		ux00ddr_enablevreftraining(DDRC_BASE);
	/* mask off interrupts for leveling completion */
	ux00ddr_mask_leveling_completed_interrupt(DDRC_BASE);

	ux00ddr_mask_mc_init_complete_interrupt(DDRC_BASE);
	ux00ddr_mask_outofrange_interrupts(DDRC_BASE);
	ux00ddr_setuprangeprotection(DDRC_BASE,DDR_SIZE);
	ux00ddr_mask_port_command_error_interrupt(DDRC_BASE);

	ux00ddr_start(DDRC_BASE, PHYSICAL_FILTER_CTRL_ADDR, ddr_end);
	ux00ddr_phy_fixup(DDRC_BASE);
}
